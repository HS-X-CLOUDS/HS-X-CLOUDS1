import aiohttp
import logging

logger = logging.getLogger(__name__)
logger.setLevel(logging.INFO)

async def get_verify_shorted_link(link):
    SHORTNER_API = "a33ce12542055ff79361d9bcc681ece612bdcaf8"
    SHORTNER_URL = "kpslink.in"
    https = link.split(":")[0]
    if "http" == https:
        https = "https"
        link = link.replace("http", https)
    url = f'https://{SHORTNER_URL}/api'
    params = {'api': SHORTNER_API,
              'url': link,
              }

    try:
        async with aiohttp.ClientSession() as session:
            async with session.get(url, params=params, raise_for_status=True, ssl=False) as response:
                data = await response.json()
                if data["status"] == "success":
                    return data['shortenedUrl']
                else:
                    logger.error(f"Error: {data['message']}")
                    return f'https://{SHORTNER_URL}/api?api={SHORTNER_API}&link={link}'

    except Exception as e:
        logger.error(e)
        return f'{SHORTNER_URL}/api?api={SHORTNER_API}&link={link}'


